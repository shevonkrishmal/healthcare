package com.example.healthcare;

public class NeedHelp {
    private String nameH;
    private String emailH;
    private Integer contactH;
    private String msgH;

   public NeedHelp(){

   }

    public String getNameH() {
        return nameH;
    }

    public void setNameH(String nameH) {
        this.nameH = nameH;
    }

    public String getEmailH() {
        return emailH;
    }

    public void setEmailH(String emailH) {
        this.emailH = emailH;
    }

    public Integer getContactH() {
        return contactH;
    }

    public void setContactH(Integer contactH) {
        this.contactH = contactH;
    }

    public String getMsgH() {
        return msgH;
    }

    public void setMsgH(String msgH) {
        this.msgH = msgH;
    }
}
